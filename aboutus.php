<h2>A bit about us</h2>
<p>The Christmas draw was established in 2011 as a one off draw to help raise funds for the maternity unit in Letterkenny University Hospital. The draw was a huge success and raised a fantastic sum of &euro;4,000. </p>
<p>The draw then returned in 2013 due to the Phillipines disaster and it was run again and raised a further &euro;1,000 which went directly to helping those displaced in the Phillipines because of the storms.</p>
<p>It has since become a stable part of the Christmas Calendar, it has been run successfully every year since 2013 and alongside 2 fun walks in 2011 and 2012 it has meant that to date almost &euro;25,000 has been raised for various charities and organisations including I.C.A.R.E., St. Mary's National School Stranorlar, R.N.L.I. lough Swilly Branch and others.</p>
<p>Apart from the Phillipines disaster in 2013 all the money that has been raised has been donated locally. The aim of the draw is to help raise funds for those smaller local charities and organisations that struggle on a daily basis to provide services to the local people of the community.</p>
<p>This years beneficiary is the Kevin Bell Repatriation Trust. The KBRT repatriate Irish people who have sadly passed away abroad in whatever circumstances that has caused their passing. They receive all their funding from local people who have had to avail of their services or by donations from people who have learned about the Charity. They take over the repatriation of your loved one back to Ireland, working with the police, hospitals and government agencies in those countries where the loved one has passed away. you can find out more information the Charity by entering the link here into your web browser www.kevinbellrepatriationtrust.com </p>
